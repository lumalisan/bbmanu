unsigned char count = 0;

/******************************************************************************/
/* Procedures declaration                                                     */
/******************************************************************************/

/******************************************************************************/
/* TASKS declaration and implementation                                       */
/******************************************************************************/

 /* Task states

	DESTROYED / uninitalized
	STOPPED
	WAITING
	DELAYED
	ELIGIBLE /READY
	RUNNING

*/


void TaskA (void)
{
	unsigned char i;

	while(1)
	{
		for (i=0; i < 15; i++)
			Delay15ms();

		count = (count + 1) % 64;
		OS_Yield(); // Indica al SO que quiere salir de la CPU
	}
}


void TaskB (void)
{
	unsigned char i;

	while(1)
	{
		for (i=0; i < 15; i++)
			Delay15ms();

		printNumInLED(count);
        printf("DEBUG: %d\n", count);
		OS_Yield();
	}
}


/******************************************************************************/
/* Interrupts                                                                 */
/******************************************************************************/


/******************************************************************************/
/* Main                                                                       */
/******************************************************************************/

int main (void)
{

	// ===================
	// Init peripherals
	// ===================
	initLEDs();

	// =========================
	// Create Salvo structures
	// =========================
	// Init Salvo
	OSInit();

	// Create tasks (name, tcb, priority) and push them to ELIGIBLE STATE
	// From 1 up to OSTASKS tcbs available
	// Priorities from 0 (highest) down to 15 (lowest)
	OSCreateTask(TaskA, OSTCBP(1), 10);
	OSCreateTask(TaskB, OSTCBP(2), 10);

	// =============================================
	// Enter multitasking envinronment
	// =============================================
	while (1)
	{
		OSSched();
	}

}


